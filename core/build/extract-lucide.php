<?php
// core/build/extract-lucide.php — regenerate core/lib/lucide-icons.php from
// the upstream lucide package on npm. Run after editing $icons_needed
// when you want to bundle a new icon name into the curated set.
//
//   php core/build/extract-lucide.php
//
// Pulls lucide@LUCIDE_VERSION (pinned at the top of this file) from the
// npm registry, unpacks the ESM icon source files, parses out their
// path tuples with a regex, and writes a flat PHP array. The output is
// committed to the repo — this script is build-time only and never runs
// in production.

declare(strict_types=1);

const LUCIDE_VERSION  = '0.469.0';
const ICONS_NEEDED    = [
    // Hardcoded in section templates / page templates
    'zap', 'arrow-right', 'arrow-left', 'check', 'chevron-down',
    // Seeded into the content_blocks table (see site/migrations/0001_seed.sql)
    'calendar-days', 'sparkles', 'users', 'message-square-text',
    'file-output', 'layout-template', 'building-2', 'calendar-plus',
    'wand-sparkles', 'send', 'user', 'briefcase', 'building',
    // Common extras — covers most editorial swaps without bloating the bundle
    'bot', 'mail', 'phone', 'settings', 'search', 'plus', 'x',
    'info', 'triangle-alert', 'star', 'trash', 'download', 'upload',
    'pencil', 'copy', 'link', 'lock', 'eye', 'menu',
    'chevron-up', 'chevron-left', 'chevron-right', 'arrow-up', 'arrow-down',
];

if (PHP_SAPI !== 'cli') {
    fwrite(STDERR, "CLI only\n");
    exit(2);
}

$tmp = sys_get_temp_dir() . '/lucide-extract-' . getmypid();
mkdir($tmp);
register_shutdown_function(function () use ($tmp) {
    shell_exec('rm -rf ' . escapeshellarg($tmp));
});

$tarball = 'https://registry.npmjs.org/lucide/-/lucide-' . LUCIDE_VERSION . '.tgz';
echo "Fetching $tarball ...\n";
$ctx = stream_context_create(['http' => ['timeout' => 30]]);
$tgz = @file_get_contents($tarball, false, $ctx);
if ($tgz === false || strlen($tgz) < 1000) {
    fwrite(STDERR, "Failed to download tarball (got " . strlen((string)$tgz) . " bytes)\n");
    exit(1);
}
file_put_contents("$tmp/lucide.tgz", $tgz);

$result  = [];
$skipped = [];
foreach (ICONS_NEEDED as $name) {
    $rel = "package/dist/esm/icons/$name.js";
    shell_exec(sprintf('cd %s && tar -xzf lucide.tgz %s 2>/dev/null', escapeshellarg($tmp), escapeshellarg($rel)));
    $file = "$tmp/$rel";
    if (!is_file($file)) {
        $skipped[] = "$name (not in lucide@" . LUCIDE_VERSION . ')';
        continue;
    }
    $src = file_get_contents($file);

    // Pull every ["tag", {attrs}] tuple. Tolerate whitespace/newlines.
    if (!preg_match_all('/\[\s*"(\w+)"\s*,\s*(\{[^}]*\})\s*\]/s', $src, $matches, PREG_SET_ORDER)) {
        $skipped[] = "$name (parse failed)";
        continue;
    }
    $elements = [];
    foreach ($matches as $m) {
        $tag = $m[1];
        if ($tag === 'svg') continue;  // outer wrapper, we emit it ourselves
        $attrs_block = $m[2];
        $attrs = [];
        // Match key: "value" or "key": "value", tolerating escaped quotes in value.
        if (preg_match_all('/(?:"([\w-]+)"|([\w-]+))\s*:\s*"((?:[^"\\\\]|\\\\.)*)"/', $attrs_block, $am, PREG_SET_ORDER)) {
            foreach ($am as $a) {
                $key = $a[1] !== '' ? $a[1] : $a[2];
                $attrs[$key] = stripcslashes($a[3]);
            }
        }
        if ($attrs !== []) {
            $elements[] = ['tag' => $tag, 'attrs' => $attrs];
        }
    }
    if ($elements === []) {
        $skipped[] = "$name (no elements)";
        continue;
    }
    $result[$name] = $elements;
}

$out_file = __DIR__ . '/../lib/lucide-icons.php';
$content  = "<?php\n";
$content .= "// core/lib/lucide-icons.php — generated by core/build/extract-lucide.php\n";
$content .= "// from lucide@" . LUCIDE_VERSION . ". Re-run that script after editing the\n";
$content .= "// ICONS_NEEDED list. ISC license: https://lucide.dev.\n";
$content .= "// Hand edits will be lost on regen.\n\n";
$content .= "return " . var_export($result, true) . ";\n";

file_put_contents($out_file, $content);

echo "Wrote " . count($result) . " icons to " . realpath($out_file) . "\n";
if ($skipped) {
    fwrite(STDERR, "Skipped:\n  - " . implode("\n  - ", $skipped) . "\n");
}
